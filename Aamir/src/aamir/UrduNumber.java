public class UrduNumber {
    public static void main(String[] args) {
        String No="\u0661";
        System.out.println(No);
        No="\u0662";
        System.out.println(No);
        No="\u0663";
        System.out.println(No);
        No="\u0664";
        System.out.println(No);
        No="\u0665";
        System.out.println(No);
        No="\u0666";
        System.out.println(No);
        No="\u0667";
        System.out.println(No);
        No="\u0668";
        System.out.println(No);
        No="\u0669";
        System.out.println(No);
        No="\u0661\u0660";
        System.out.println(No);
    }
    
}
